// Faça um programa que leia um número inteiro e mostre o seu antecessor e seu
// sucessor.
// Ex:
// Digite um número: 9
// O antecessor de 9 é 8
// O sucessor de 9 é 10

let numero = 0;
let ant = numero - 1;
let suc = numero + 1;

console.log(`O antecessor do numero ${numero} é ${ant}
O sucessor do numero ${numero} é ${suc}`);